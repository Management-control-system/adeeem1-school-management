/**
 * One-time data uploading (Migration from localStorage to Cloud Firestore)
 */
export async function uploadLocalDataToFirebase(schoolId: string, students: Student[], teachers: Teacher[], pins: SavedPin[]) {
  if (!db) return false;
  try {
    const batch = writeBatch(db);
    
    // Migrate students
    for (const s of students) {
      const docRef = doc(db, 'schools', schoolId, 'students', s.id);
      batch.set(docRef, s);
    }
    
    // Migrate teachers
    for (const t of teachers) {
      const docRef = doc(db, 'schools', schoolId, 'teachers', t.id);
      batch.set(docRef, t);
    }

    // Migrate pins
    for (const p of pins) {
      const docRef = doc(db, 'schools', schoolId, 'pins', p.id);
      batch.set(docRef, p);
    }
    
    await batch.commit();
    return true;
  } catch (error) {
    handleFirestoreError(error, OperationType.WRITE, `schools/${schoolId}/batch-migration`);
    return false;
  }
}
